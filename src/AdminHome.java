    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String email = model.getValueAt(index, 1).toString(); // car l'eamail dans le design est a 1 et nom a 0
        String status = model.getValueAt(index, 4).toString();
        if(status.equals("true"))
            status = "false";
        else
            status = "true";
        try{
            int a = JOptionPane.showConfirmDialog(null,"Veux tu changer le status?"+email+" ","select",JOptionPane.YES_NO_OPTION);
            if(a==0){
                InsertUpdateDelete.setData("update users set statut ='"+status+"' where email='"+email+"' ", "Status modifi√©");
                setVisible(false);
                new AdminHome().setVisible(true);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jTable1MouseClicked

